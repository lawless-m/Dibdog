% AND binds tighter than OR. The generator must insert parens
% wherever default precedence + left-associativity would re-parse to
% a different AST than the one we're generating. Cases:
%
%   AND/L: wrap if L is or(_,_)  -- precedence preservation
%   AND/R: wrap if R is or(_,_)  -- precedence preservation
%          wrap if R is and(_,_) -- right-assoc preservation
%                                   (default re-parse is left-assoc)
%   OR/L:  never wrap  (lowest precedence, left-assoc default)
%   OR/R:  wrap if R is or(_,_)  -- right-assoc preservation
gen_predicate(and(L, R), Chars) :-
    gen_pred_paren_if(L, [or], LC),
    gen_pred_paren_if(R, [or, and], RC),
    append([LC, [' ','A','N','D',' '], RC], Chars).
gen_predicate(or(L, R), Chars) :-
    gen_predicate(L, LC),
    gen_pred_paren_if(R, [or], RC),
    append([LC, [' ','O','R',' '], RC], Chars).

% Wrap predicate P in parens iff its outer functor is in the WrapList.
gen_pred_paren_if(P, WrapList, Chars) :-
    ( functor(P, F, _),
      member(F, WrapList)
    -> gen_predicate(P, PC),
       append([['('], PC, [')']], Chars)
    ;  gen_predicate(P, Chars)
    ).
