% Predicate precedence (loosest binding to tightest, SQL-standard):
%   predicate_or   = predicate_and ( OR predicate_and )*    -- left-assoc
%   predicate_and  = predicate_atom ( AND predicate_atom )* -- left-assoc
%   predicate_atom = <eq> | <is null> | <is not null>
%
% Single-predicate WHERE clauses produce the same AST as before this
% slice (just the bare atom, no `and(...)` / `or(...)` wrapper), so
% existing meaningful entries don't need expected.term migration.

predicate_or(P)   --> predicate_and(P1), or_rest(P1, P).
or_rest(L, P)     --> ws1, keyword(or),  ws1, predicate_and(R), or_rest(or(L, R), P).
or_rest(P, P)     --> [].

predicate_and(P)  --> predicate_atom(P1), and_rest(P1, P).
and_rest(L, P)    --> ws1, keyword(and), ws1, predicate_atom(R), and_rest(and(L, R), P).
and_rest(P, P)    --> [].

% Parenthesised sub-predicates are placed AT the atom level so they
% compose correctly with the precedence chain — `(a AND b) OR c` parses
% predicate_or → predicate_and → predicate_atom(`(a AND b)` → `and(a,b)`)
% then or_rest matches the OR and returns `or(and(a,b), c)`.
predicate_atom(P) -->
    ['('], ws, predicate_or(P), ws, [')'].
% NOT prefix. Probed slice #76: DBISAM accepts both `NOT (pred)` and
% bare `NOT pred`. Standard SQL precedence is NOT > AND > OR, which
% emerges naturally by placing NOT at atom level (it binds tightest).
% Order: try NOT before is_not_null so the leading `NOT` consumes
% correctly. is_not_null consumes a value-then-is-not-null pattern,
% so it won't fire on a bare leading NOT — but explicit ordering
% keeps the intent clear.
predicate_atom(not(P)) -->
    keyword(not), ws1,
    predicate_atom(P).
% Ordering matters: try IS [NOT] NULL first (the IS keyword would
% otherwise parse as an identifier). For symbolic comparison operators,
% try the longer prefixes first (`<=`, `>=`, `<>`, `!=` before `<`,
% `>`, `=`) so the lexer doesn't half-consume them.
predicate_atom(is_not_null(L)) -->
    value(L),
    ws1, keyword(is),
    ws1, keyword(not),
    ws1, keyword(null).
predicate_atom(is_null(L)) -->
    value(L),
    ws1, keyword(is),
    ws1, keyword(null).
% IN / NOT IN. RHS is either a value list `(v1, v2, ...)` or a
% single subselect `(<SELECT ...>)`. Subselect wrapped in
% `subselect(SelectTerm)` to keep value lists (list shape) and
% subqueries (functor shape) distinguishable in the AST.
%
% DBISAM accepts subqueries in EXACTLY this position — full rules in
% `dbisam-fussy-subqueries` memory file. Grammar deliberately
% over-accepts correlation and nesting; engine catches those at
% parse time and any corpus entry that exercises them gets
% `expected-divergent` with a documented [divergence] block.
%
% Order matters in the alternatives: try subselect first because
% `keyword(select)` is a stronger discriminator than the start of a
% value expression. Bare `value_list` could start with an identifier
% spelled `select` (unlikely but possible) — but the keyword match
% requires a non-identifier char after it (handled by ws1 in the
% nested rule), so backtracking still resolves correctly.
predicate_atom(not_in(L, Rhs)) -->
    value(L),
    ws1, keyword(not),
    ws1, keyword(in),
    ws, ['('], ws,
    in_rhs(Rhs),
    ws, [')'].
predicate_atom(in(L, Rhs)) -->
    value(L),
    ws1, keyword(in),
    ws, ['('], ws,
    in_rhs(Rhs),
    ws, [')'].

in_rhs(subselect(S)) --> select_chain(S).
in_rhs(Vs) --> value_list(Vs).
% BETWEEN / NOT BETWEEN — range predicate. Order: try NOT BETWEEN
% before BETWEEN so the NOT prefix gets consumed correctly.
% Probed accepted on rivsem04: bare numeric, qualified-col, `?`
% params on both Lo and Hi, NOT BETWEEN. Lo/Hi are full value
% expressions per standard.
predicate_atom(not_between(V, Lo, Hi)) -->
    value(V),
    ws1, keyword(not),
    ws1, keyword(between),
    ws1, value(Lo),
    ws1, keyword(and),
    ws1, value(Hi).
predicate_atom(between(V, Lo, Hi)) -->
    value(V),
    ws1, keyword(between),
    ws1, value(Lo),
    ws1, keyword(and),
    ws1, value(Hi).
predicate_atom(not_like(L, R)) -->
    value(L),
    ws1, keyword(not),
    ws1, keyword(like),
    ws1, string_literal(R).
predicate_atom(like(L, R)) -->
    value(L),
    ws1, keyword(like),
    ws1, string_literal(R).
predicate_atom(cmp(Op, L, R)) -->
    value(L),
    ws, cmp_op(Op), ws,
    value(R).
predicate_atom(eq(L, R)) -->
    value(L),
    ws, ['='], ws,
    value(R).

% Comparison operators — longer prefixes first.
% `<>` and `!=` both mean "not equal"; canonicalise to `ne`.
cmp_op(le) --> ['<','='].
cmp_op(ge) --> ['>','='].
cmp_op(ne) --> ['<','>'].
cmp_op(ne) --> ['!','='].
cmp_op(lt) --> ['<'].
cmp_op(gt) --> ['>'].

% Comma-separated value list (for IN).
value_list([V|Vs]) -->
    value(V),
    value_list_rest(Vs).

value_list_rest([]) --> [].
value_list_rest([V|Vs]) -->
    ws, [','], ws,
    value(V),
    value_list_rest(Vs).

% A value expression. Standard SQL arithmetic precedence chain:
%   additive       — left-assoc `+`, `-`, `||`
%   multiplicative — left-assoc `*`, `/`, `MOD`
%   unary          — prefix `-`
%   primary        — literals, idents, function calls, parens, CAST
%
% DBISAM-specific probing on rivsem04 (2026-05-27): `%` is rejected
% (use `MOD` instead); `||` and string `+` both work for concatenation
% (engine type-checks at parse time); `&` is rejected. Unary minus
% works on both literals and column references. See probe results in
% the slice #37 notes.
value(V) --> additive(V).

additive(A) -->
    multiplicative(L),
    add_rest(L, A).
add_rest(L, A) --> ws, ['+'], ws, multiplicative(R), add_rest(arith(add, L, R), A).
add_rest(L, A) --> ws, ['-'], ws, multiplicative(R), add_rest(arith(sub, L, R), A).
add_rest(L, A) --> ws, ['|','|'], ws, multiplicative(R), add_rest(arith(concat, L, R), A).
add_rest(V, V) --> [].

multiplicative(M) -->
    unary(L),
    mul_rest(L, M).
mul_rest(L, M) --> ws, ['*'], ws, unary(R), mul_rest(arith(mul, L, R), M).
mul_rest(L, M) --> ws, ['/'], ws, unary(R), mul_rest(arith(div, L, R), M).
mul_rest(L, M) --> ws1, keyword(mod), ws1, unary(R), mul_rest(arith(mod, L, R), M).
mul_rest(V, V) --> [].

unary(neg(V)) --> ['-'], ws, unary(V).
unary(V) --> primary(V).

% Primary: a value with no infix arithmetic above it. Same paren-strip
% rule as projection items. Order matters: try the longer prefixes
% (CAST, function_call, qualified) before bare identifier so `a.b`
% doesn't get half-consumed as `identifier(a)` followed by stray
% `.b`, and `SUM(x)` doesn't get half-consumed as `identifier(SUM)`.
% The paren branch recurses back into `value//1` so a parenthesised
% subexpression resets the precedence level (`(1+2)*3` works).
primary(V) --> ['('], ws, value(V), ws, [')'].
primary(V) --> cast_expr(V).
primary(V) --> case_expr(V).
primary(V) --> function_call(V).
primary(qualified(Q, N)) -->
    identifier(Q), ['.'], identifier(N).
% Boolean literals. Probed slice #73: `TRUE` and `FALSE` are
% accepted by the engine as value literals (e.g., `WHERE col = TRUE`
% works). Order BEFORE identifier so TRUE/FALSE consume as literals,
% not as column references. Existing corpus entries that compared
% against TRUE (0109, 0113) get a richer AST after this change.
primary(boolean_literal(true))  --> keyword(true).
primary(boolean_literal(false)) --> keyword(false).
primary(V) --> identifier(V).
primary(V) --> decimal_literal(V).
primary(V) --> integer_literal(V).
primary(V) --> string_literal(V).
% JDBC-style positional parameter. DBISAM has native support — `?`
% is accepted as a value-position placeholder in WHERE, JOIN ON,
% INSERT VALUES, and similar slots. Probed slice (post-template-
% harvest): `select … where col = ?` and `insert into t values (?, ?, …)`
% both parsed by the engine. No name/index in the AST; positional
% form only (`:name` and bracketed `[?param]` rejected by engine).
primary(parameter(positional)) --> ['?'].

% CASE expression — two forms, both terminated by END.
%   Searched: CASE WHEN <pred> THEN <val> [...]* [ELSE <val>] END
%   Simple:   CASE <expr> WHEN <val> THEN <val> [...]* [ELSE <val>] END
%
% AST:
%   case_when([when(Cond, Result), ...], else(Val) | no_else)
%   case_match(Expr, [when(Val, Result), ...], else(Val) | no_else)
%
% Try the searched form first by peeking for `WHEN` immediately after
% `CASE` — that disambiguates with no real backtracking cost. The
% simple form falls out as the alternative.
%
% Probed on rivsem04: both forms accept ELSE-less variants, multiple
% WHEN clauses, nesting, use as projection or as operand of arith.
case_expr(C) -->
    keyword(case),
    ws1,
    case_body(C),
    ws1,
    keyword(end).

case_body(case_when(Whens, Else)) -->
    case_when_list_searched(Whens),
    case_else_opt(Else).
case_body(case_match(Expr, Whens, Else)) -->
    value(Expr),
    ws1,
    case_when_list_simple(Whens),
    case_else_opt(Else).

case_when_list_searched([when(C, R) | Rest]) -->
    keyword(when), ws1,
    predicate_or(C), ws1,
    keyword(then), ws1,
    value(R),
    case_when_list_searched_more(Rest).
case_when_list_searched_more([]) --> [].
case_when_list_searched_more(L) -->
    ws1, case_when_list_searched(L).

case_when_list_simple([when(V, R) | Rest]) -->
    keyword(when), ws1,
    value(V), ws1,
    keyword(then), ws1,
    value(R),
    case_when_list_simple_more(Rest).
case_when_list_simple_more([]) --> [].
case_when_list_simple_more(L) -->
    ws1, case_when_list_simple(L).

case_else_opt(else(V)) -->
    ws1, keyword(else), ws1,
    value(V).
case_else_opt(no_else) --> [].

% CAST(<expr> AS <type>) — special form. `<type>` is one of DBISAM's
% built-in type keywords with an optional parenthesised size (only
% applicable to VARCHAR / CHAR; ignored by the grammar for other types).
% Case-insensitive on CAST, AS, and the type names. AST canonicalises
% the type name to UPPERCASE so casts to `varchar(255)` and `VARCHAR(255)`
% produce the same term.
cast_expr(cast(E, T)) -->
    keyword(cast),
    ws, ['('], ws,
    value(E),
    cast_separator,
    cast_type(T),
    ws, [')'].

% DBISAM accepts both `CAST(expr AS type)` (SQL standard) and
% `CAST(expr, type)` (DBISAM extension). The separator surfaces only
% during parsing; the AST and generator emit the canonical AS-form.
cast_separator --> ws1, keyword(as), ws1.
cast_separator --> ws, [','], ws.

cast_type(type(TypeAtom, [Size])) -->
    type_keyword(TypeAtom),
    ws, ['('], ws, integer_literal(integer_literal(Size)), ws, [')'].
cast_type(type(TypeAtom, [])) -->
    type_keyword(TypeAtom).

% Function call: `<NAME>(<arg-list>)` where NAME must be in the
% verified `valid_function/1` catalogue (auto-generated from live
% engine probing — see grammar/functions.pl). Calls to unknown
% names fail to parse, matching the engine's parse-time validation.
%
% The argument list is comma-separated values, with one special
% form: `COUNT(*)` and any other valid wildcard arg list `[star]`.
% Mixed forms (`COUNT(a, *)`) are syntactically accepted; the engine
% rejects them, but matching that strictness would require knowing
% per-function arity (deferred — function_arg_shape/2 has some of
% this but enforcement is for a future slice).
%
% EXTRACT is a special form: `EXTRACT(<field> FROM <expr>)`. Field is
% a bareword keyword from a fixed set (DBISAM tells us via the engine
% error: YEAR/MONTH/WEEK/DAY/DAYOFWEEK/DAYOFYEAR/HOUR/MINUTE/SECOND/
% MSECOND). EXTRACT is tried before the general function-call rule so
% its `<field> FROM ...` shape doesn't get misparsed as a 2-arg call.
%
% AST:
%   extract(FieldAtom, Expr)             -- EXTRACT(field FROM expr)
%   trim(SideAtom, Chars, Expr)          -- TRIM(side chars FROM expr)
%   function_call(NameAtom, ArgList)     -- everything else
function_call(extract(Field, Expr)) -->
    keyword(extract),
    ws, ['('], ws,
    extract_field(Field),
    ws1, keyword(from),
    ws1, value(Expr),
    ws, [')'].
% TRIM is a special form. DBISAM requires:
%   TRIM(<side> <chars-string-literal> FROM <expr>)
%   TRIM(<side> <chars-string-literal>, <expr>)    -- comma-separator variant
% Both shapes accepted; both store the same `trim/3` AST. Generator
% emits the FROM form (SQL-standard).
function_call(trim(Side, Chars, Expr)) -->
    keyword(trim),
    ws, ['('], ws,
    trim_side(Side),
    ws1, string_literal(Chars),
    ws, trim_separator,
    ws, value(Expr),
    ws, [')'].

trim_side(leading)  --> keyword(leading).
trim_side(trailing) --> keyword(trailing).
trim_side(both)     --> keyword(both).

trim_separator --> keyword(from).
trim_separator --> [','].
% CONCAT — special form: DBISAM accepts the SQL Server-ish
% `CONCAT(a WITH b)` separator in addition to the standard
% `CONCAT(a, b)`. Strictly binary either way (3-arg CONCAT rejected
% by engine — see memory `dbisam-function-quirks`). `WITH` is
% CONCAT-specific; COALESCE/IF/etc. reject it. Try the WITH form
% first; if it fails, fall through to the generic function_call rule
% which handles the comma form.
function_call(function_call('CONCAT', [A, B])) -->
    keyword(concat),
    ws, ['('], ws,
    func_arg(A),
    ws1, keyword(with),
    ws1, func_arg(B),
    ws, [')'].
function_call(function_call(Name, Args)) -->
    identifier(identifier(NameAtom)),
    { atom_upper(NameAtom, Name), valid_function(Name) },
    ws, ['('], ws,
    func_arg_list(Args),
    ws, [')'],
    { check_function_arity(Name, Args) }.

% Arity enforcement (slice #65). Reject a function call whose arg
% count doesn't match any documented shape — unless the function is
% declared variadic, or no shapes are documented at all (in which
% case defer to the engine).
%
% `[star]` for COUNT(*) and similar wildcards is treated as length 1
% to match the documented `COUNT/1` shape.
check_function_arity(_Name, [star]) :- !.
check_function_arity(Name, _Args) :-
    function_variadic(Name), !.
check_function_arity(Name, _Args) :-
    \+ function_arg_shape(Name, _), !.
check_function_arity(Name, Args) :-
    length(Args, N),
    function_arg_shape(Name, Shape),
    length(Shape, N), !.

