% parse_statement(+Chars, -Term)
%   Run the DCG forward. Succeeds iff the input is a syntactically
%   valid statement in the current grammar scope. Fails (no error)
%   if the input doesn't parse.
parse_statement(Chars, Term) :-
    phrase(statement(Term), Chars).

