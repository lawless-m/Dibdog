% Emit an identifier atom, double-quoting if the atom isn't a valid
% bare identifier (contains spaces, starts with a digit, contains
% punctuation, etc.). Canonical form: bare when possible, quoted
% otherwise. Round-trip stable — re-parsing the quoted form produces
% the same atom.
gen_ident_atom(A, Chars) :-
    atom_chars(A, AC),
    ( bare_ident_chars_ok(AC)
    -> Chars = AC
    ;  append([['"'], AC, ['"']], Chars)
    ).

bare_ident_chars_ok([]) :- false.
bare_ident_chars_ok([C|Cs]) :-
    ident_start(C),
    all_ident_cont(Cs).

all_ident_cont([]).
all_ident_cont([C|Cs]) :- ident_cont(C), all_ident_cont(Cs).

% Atom uppercase helper. Mirrors atom_lower/2 elsewhere in this file.
atom_upper(A, U) :-
    atom_chars(A, Cs),
    maplist(to_upper, Cs, UCs),
    atom_chars(U, UCs).

to_upper(C, U) :-
    ( C @>= 'a', C @=< 'z'
    -> char_code(C, L), Uc is L - 32, char_code(U, Uc)
    ; U = C
    ).

