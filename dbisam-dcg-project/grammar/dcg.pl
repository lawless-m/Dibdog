% Whitespace — zero or more chars, treating SQL comments as
% whitespace-equivalent. Probed slice #66: DBISAM accepts both
% `--` line comments (to end-of-line) and `/* */` block comments
% (non-nesting). Block-comment nesting is rejected — `/* a /* b */ c */`
% closes at the first `*/` and the engine then chokes on the
% trailing `c */`. The grammar matches this behaviour.
%
% Cuts after a successful token-consume prevent the arithmetic `-`
% rule from undoing a `--` comment match: once we've recognised the
% double-dash as a comment, the parser commits and won't backtrack
% to split the `--` into two unary-minus tokens.
ws --> line_comment, !, ws.
ws --> block_comment, !, ws.
ws --> [C], { ws_char(C) }, ws.
ws --> [].

% Whitespace — one or more. Same alternatives as ws but at least
% one match is required.
ws1 --> line_comment, !, ws.
ws1 --> block_comment, !, ws.
ws1 --> [C], { ws_char(C) }, ws.

ws_char(' ').
ws_char('\t').
ws_char('\n').
ws_char('\r').

% `--` to end-of-line (or end-of-input). Engine probed (slice #66):
% line comment terminates at any line-ending character; entire
% comment is treated as whitespace.
line_comment --> ['-', '-'], line_comment_tail.

line_comment_tail --> ['\n'].
line_comment_tail --> ['\r'].
line_comment_tail --> [_], line_comment_tail.
line_comment_tail --> [].                            % EOF (no newline)

% `/* */` block comment, NOT nesting. Engine probed: nested forms
% close at the first `*/` and trail garbage afterwards. The grammar
% matches that by being non-nesting too.
block_comment --> ['/', '*'], block_comment_tail.

block_comment_tail --> ['*', '/'].
block_comment_tail --> [_], block_comment_tail.

% Identifier — either bare (letter/underscore + alnum/underscore) or
% double-quoted (any chars except embedded `"`). Both produce the
% same `identifier(Atom)` AST term; the atom carries the contained
% text verbatim. DBISAM accepts the quoted form anywhere a bare one
% goes — column refs, table refs, aliases, qualified-ident parts.
identifier(identifier(Atom)) -->
    ['"'],
    quoted_ident_chars(Cs),
    ['"'],
    { Cs \= [], atom_chars(Atom, Cs) }.
% Square-bracket form. Probed on rivsem04 (slice #40): DBISAM accepts
% `[code]`, `[CUSTOMER]`, `CUSTOMER.[code]`, `as [alias]`, etc., but
% rejects bracketed forms whose contents aren't a valid bare identifier
% (`[some col]`, `[123]`, `[order#]` all reject). So this is a
% delimiter variant — same char class as bare — not a SQL-Server-style
% quoted identifier that admits arbitrary chars.
identifier(identifier(Atom)) -->
    ['['],
    [C], { ident_start(C) },
    ident_rest(Cs),
    [']'],
    { atom_chars(Atom, [C|Cs]) }.
identifier(identifier(Atom)) -->
    [C], { ident_start(C) },
    ident_rest(Cs),
    { atom_chars(Atom, [C|Cs]) }.

quoted_ident_chars([C|Cs]) --> [C], { C \== '"' }, quoted_ident_chars(Cs).
quoted_ident_chars([]) --> [].

ident_start(C) :- alpha_char(C).
ident_start('_').

ident_rest([C|Cs]) --> [C], { ident_cont(C) }, ident_rest(Cs).
ident_rest([]) --> [].

ident_cont(C) :- alpha_char(C).
ident_cont(C) :- digit_char(C).
ident_cont('_').

alpha_char(C) :- C @>= 'a', C @=< 'z'.
alpha_char(C) :- C @>= 'A', C @=< 'Z'.
digit_char(C) :- C @>= '0', C @=< '9'.

% Integer literal — not used by the seed corpus's SELECT path yet,
% but defined now because the next likely slice (WHERE with numeric
% predicates) needs it. Exposed via `integer_literal//1`.
integer_literal(integer_literal(N)) -->
    [D], { digit_char(D) },
    digits(Ds),
    { number_chars(N, [D|Ds]) }.

digits([D|Ds]) --> [D], { digit_char(D) }, digits(Ds).
digits([]) --> [].

% Decimal / float literal. Probed slice #61: DBISAM accepts every
% common shape — `3.14`, `0.5`, `.5`, `5.`, `1.5e3`, `1.5E-3`, `1e3`.
% Stored as `decimal_literal(Atom)` with the textual representation
% in the atom to preserve exactness on round-trip (avoids any
% binary-float rounding). Must be tried BEFORE `integer_literal` in
% primary//1 so `1.5` doesn't get truncated to integer `1`.
%
% Shapes admitted:
%   <digits>.<digits>[exp]    — `3.14`, `3.14e5`
%   <digits>.[exp]            — `5.`, `5.e3`
%   .<digits>[exp]            — `.5`, `.5e3`
%   <digits><exp>             — `1e3`, `1E-5`
% where <exp> is `[eE] [+-]? <digits>`.
decimal_literal(decimal_literal(Atom)) -->
    dec_with_dot(Cs),
    { atom_chars(Atom, Cs) }.
decimal_literal(decimal_literal(Atom)) -->
    digits1(Is),
    exp_part(Es),
    { append(Is, Es, Cs), atom_chars(Atom, Cs) }.

dec_with_dot(Cs) -->
    digits(Is),
    ['.'],
    digits(Fs),
    exp_opt(Es),
    { ( Is \= [] ; Fs \= [] ),
      append([Is, ['.'], Fs, Es], Cs) }.

digits1([D|Ds]) --> [D], { digit_char(D) }, digits(Ds).

exp_opt(Es) --> exp_part(Es).
exp_opt([])  --> [].

exp_part([E|Rest]) -->
    [E], { e_char(E) },
    sign_opt(Sign),
    digits1(Ds),
    { append(Sign, Ds, Rest) }.

sign_opt(['+']) --> ['+'].
sign_opt(['-']) --> ['-'].
sign_opt([])    --> [].

e_char('e').
e_char('E').

% Single-quoted string literal with standard-SQL doubled-quote
% escape. Probed slice #60: DBISAM accepts `'O''Brien'` and
% interprets it as the 7-char string `O'Brien` (per SQL standard).
% Backslash sequences (`\n`, `\'`) are NOT interpreted — backslash
% is just a literal character inside a string. The grammar therefore
% only needs to handle the doubled-quote escape; other characters
% (including `\`) pass through verbatim.
%
% Order in `string_chars` matters: try the doubled-quote `''` clause
% FIRST so a `'` followed by another `'` doesn't terminate the
% string prematurely. The atom built into the AST holds the decoded
% content (one `'` per source-level `''`).
string_literal(string_literal(Atom)) -->
    [''''],
    string_chars(Cs),
    [''''],
    { atom_chars(Atom, Cs) }.

string_chars([''''|Cs]) --> ['''', ''''], string_chars(Cs).
string_chars([C|Cs])    --> [C], { C \== '''' }, string_chars(Cs).
string_chars([]) --> [].

% Keyword — case-insensitive match against an atom argument.
keyword(K) -->
    { atom_chars(K, KChars) },
    keyword_chars(KChars).

keyword_chars([]) --> [].
keyword_chars([K|Ks]) -->
    [C], { char_eq_ci(C, K) },
    keyword_chars(Ks).

char_eq_ci(A, B) :- to_lower(A, L), to_lower(B, L).

to_lower(C, L) :-
    ( C @>= 'A', C @=< 'Z'
    -> char_code(C, U), Lc is U + 32, char_code(L, Lc)
    ; L = C
    ).
