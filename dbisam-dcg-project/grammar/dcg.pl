% ============================================================
% Statement
% ============================================================

% Public entry: a top-level statement is either an UPDATE or a
% SELECT (possibly chained by UNION/UNION ALL). UPDATE first because
% it has a distinctive opening keyword and there's no ambiguity.
%
% `select_chain//1` is the SELECT-or-UNION-tree predicate, used both
% here AND inside IN-subqueries (`in_rhs`). Keeping it separate from
% `statement//1` means subqueries can't accidentally accept UPDATE
% syntax — DBISAM rejects `WHERE col IN (UPDATE …)` at parse time,
% and the grammar should match that strictness.
statement(S) -->
    ws,
    statement_body(S),
    trailing_semicolon_opt.

statement_body(S) --> update_statement(S).
statement_body(S) --> delete_statement(S).
statement_body(S) --> insert_statement(S).
statement_body(S) --> drop_table_statement(S).
statement_body(S) --> drop_index_statement(S).
statement_body(S) --> create_index_statement(S).
statement_body(S) --> alter_table_statement(S).
statement_body(S) --> rename_statement(S).
statement_body(S) --> maint_statement(S).
statement_body(S) --> export_statement(S).
statement_body(S) --> import_statement(S).
statement_body(S) --> transaction_statement(S).
statement_body(S) --> select_chain(S).

select_chain(S) -->
    select_statement(L),
    union_rest(L, S).

% Optional trailing `;`. DBISAM accepts (and ignores) a single
% semicolon after a top-level statement — Power-BI-generated SQL
% routinely appends one. No AST node; the `;` is not load-bearing.
trailing_semicolon_opt --> ws, [';'], ws.
trailing_semicolon_opt --> [].

% UPDATE — single-table only. Probed on rivsem04 (slice #44):
%   UPDATE <table> SET <col> = <expr> [, <col> = <expr> ...]
%                  [WHERE <pred>]
% Multi-column SET is supported (the engine accepts comma-separated
% assignments); rejections we initially saw turned out to be schema
% validation on column names. Qualified LHS (`product.price = 0`)
% accepts. No table-alias form needed for now — corpus uses bare.
update_statement(update_statement(Table, Sets, WhereOpt)) -->
    keyword(update),
    ws1, identifier(Table),
    ws1, keyword(set),
    ws1, set_clause_list(Sets),
    where_opt(WhereOpt),
    ws.

set_clause_list([S|Ss]) -->
    set_clause(S),
    set_clause_list_rest(Ss).
set_clause_list_rest([]) --> [].
set_clause_list_rest([S|Ss]) -->
    ws, [','], ws,
    set_clause(S),
    set_clause_list_rest(Ss).

set_clause(set(L, V)) -->
    set_lhs(L),
    ws, ['='], ws,
    value(V).

set_lhs(qualified(Q, N)) --> identifier(Q), ['.'], identifier(N).
set_lhs(L) --> identifier(L).

where_opt(where(P)) --> ws1, keyword(where), ws1, predicate_or(P).
where_opt(no_where) --> [].

% INSERT — two source forms, probed on rivsem04 (slice #49):
%   INSERT INTO <table> [(<col>, ...)] VALUES (<expr>, ...)
%   INSERT INTO <table> [(<col>, ...)] <SELECT-or-UNION-chain>
%
% Notable DBISAM restrictions (engine rejects at parse time):
%   • Multi-row VALUES (`VALUES (1), (2)`) is REJECTED with
%     "Expected end of statement but instead found ,". Single tuple
%     only.
%   • Scalar subqueries in VALUES (`VALUES ((SELECT …))`) are also
%     rejected — consistent with the fussy-subqueries rule.
%   • Empty VALUES list `VALUES ()` is rejected.
%   • INTO keyword is required; bare `INSERT <table>` rejected.
%
% AST:
%   insert_statement(Table, ColsOpt, Source)
%     ColsOpt = cols([identifier(C), ...]) | no_cols
%     Source  = values([V, V, ...]) | from_select(SelectTerm)
insert_statement(insert_statement(Table, ColsOpt, Source)) -->
    keyword(insert),
    ws1, keyword(into),
    ws1, identifier(Table),
    ws,
    insert_cols_opt(ColsOpt),
    insert_source(Source),
    ws.

insert_cols_opt(cols(Cs)) -->
    ['('], ws, ident_list(Cs), ws, [')'], ws.
insert_cols_opt(no_cols) --> [].

insert_source(values(Vs)) -->
    keyword(values),
    ws, ['('], ws,
    value_list(Vs),
    ws, [')'].
insert_source(from_select(S)) -->
    select_chain(S).

% DELETE — single-table only. Probed on rivsem04 (slice #48):
%   DELETE FROM <table> [WHERE <pred>]
% The `FROM` keyword is required — bare `DELETE <table>` is rejected
% with "Expected FROM but instead found <name> in DELETE SQL
% statement". Table aliasing (`DELETE FROM product p WHERE p.code = …`)
% IS accepted by the engine but no corpus entry needs it, so the
% grammar stays bare-table-only for now; add alias support when a
% real-world entry surfaces.
delete_statement(delete_statement(Table, WhereOpt)) -->
    keyword(delete),
    ws1, keyword(from),
    ws1, identifier(Table),
    where_opt(WhereOpt),
    ws.

% DROP TABLE — probed on rivsem04 (slice #63). The engine silently
% accepts a drop on a missing table, so the optional `IF EXISTS`
% clause is semantically redundant but syntactically supported. The
% grammar preserves whichever form was written. Engine error on
% bare `DROP <name>`: "Expected TABLE or INDEX" — second token must
% be TABLE (we only model TABLE here; INDEX is a separate slice).
drop_table_statement(drop_table(Table, IfExists)) -->
    keyword(drop),
    ws1, keyword(table),
    ws1, drop_if_exists_opt(IfExists),
    identifier(Table),
    ws.

drop_if_exists_opt(if_exists) -->
    keyword(if),
    ws1, keyword(exists),
    ws1.
drop_if_exists_opt(no_if_exists) --> [].

% Transaction control — probed slice #64. DBISAM follows SQL
% standard exactly:
%   START TRANSACTION        (TRANSACTION is mandatory; bare START rejected)
%   COMMIT [WORK]            (TRANSACTION here rejected; only optional WORK)
%   ROLLBACK [WORK]          (same — no TRANSACTION, no savepoints)
%
% No qualifiers (READ ONLY/READ WRITE/ISOLATION LEVEL/etc.), no
% savepoints. AST: bare atoms for the three forms, with the optional
% WORK marker preserved for round-trip.
%
% Try the longer (WORK) variant first so we consume it greedily; if
% the input lacks WORK, backtracking falls to the bare form.
transaction_statement(start_transaction) -->
    keyword(start), ws1, keyword(transaction), ws.
transaction_statement(commit(work)) -->
    keyword(commit), ws1, keyword(work), ws.
transaction_statement(commit(no_work)) -->
    keyword(commit), ws.
transaction_statement(rollback(work)) -->
    keyword(rollback), ws1, keyword(work), ws.
transaction_statement(rollback(no_work)) -->
    keyword(rollback), ws.

% CREATE INDEX — probed slice #69. Engine accepts the syntax
% (reqcode 0x2b05 in our test env is operational, not parse).
% Shapes: `CREATE [UNIQUE|NOCASE] INDEX <name> ON <table> (<cols>)`.
% Per-column direction modifier (`ASC` / `DESC`) is admissible.
% UNIQUE and NOCASE are mutually exclusive modifiers per the
% statement-vocabulary memory's "CREATE TABLE, INDEX, UNIQUE, NOCASE"
% enumeration.
create_index_statement(create_index(Name, Table, Cols, Modifier)) -->
    keyword(create),
    ws1, create_index_modifier(Modifier),
    keyword(index),
    ws1, identifier(Name),
    ws1, keyword(on),
    ws1, identifier(Table),
    ws, ['('], ws,
    index_col_list(Cols),
    ws, [')'],
    ws.

create_index_modifier(unique) --> keyword(unique), ws1.
create_index_modifier(nocase) --> keyword(nocase), ws1.
create_index_modifier(plain)  --> [].

index_col_list([C|Cs]) --> index_col(C), index_col_list_rest(Cs).
index_col_list_rest([]) --> [].
index_col_list_rest([C|Cs]) -->
    ws, [','], ws,
    index_col(C),
    index_col_list_rest(Cs).

index_col(index_col(Col, desc))    --> identifier(Col), ws1, keyword(desc).
index_col(index_col(Col, asc))     --> identifier(Col), ws1, keyword(asc).
index_col(index_col(Col, default)) --> identifier(Col).

% DROP INDEX — DBISAM-specific syntax. Probed slice #69: accepts
% bare `DROP INDEX <name>` and qualified `DROP INDEX <table>.<name>`.
% The SQL-standard `DROP INDEX <name> ON <table>` form is REJECTED
% ("Expected end of statement but instead found ON").
drop_index_statement(drop_index(qualified(Table, Name))) -->
    keyword(drop),
    ws1, keyword(index),
    ws1, identifier(Table), ['.'], identifier(Name),
    ws.
drop_index_statement(drop_index(Name)) -->
    keyword(drop),
    ws1, keyword(index),
    ws1, identifier(Name),
    ws.

% EXPORT TABLE / IMPORT TABLE — bulk data interchange. Probed slice #72:
%   EXPORT TABLE <table> TO <file>
%   IMPORT TABLE <table> FROM <file>
% TABLE keyword required, table name required, TO/FROM keyword
% required, file name required. The file name slot accepts an
% IDENTIFIER (bare, double-quoted, or bracketed) — NOT a string
% literal: `EXPORT TABLE foo TO 'foo.csv'` is REJECTED with
% "Expected file name but instead found 'foo.csv'". So a path with
% slashes/dots has to be double-quoted: `EXPORT TABLE foo TO "/tmp/foo.csv"`.
% This is unusual (most engines treat file paths as string literals)
% but it's how DBISAM was modelled — file names are object names.
export_statement(export_table(Table, File)) -->
    keyword(export),
    ws1, keyword(table),
    ws1, identifier(Table),
    ws1, keyword(to),
    ws1, identifier(File),
    ws.

import_statement(import_table(Table, File)) -->
    keyword(import),
    ws1, keyword(table),
    ws1, identifier(Table),
    ws1, keyword(from),
    ws1, identifier(File),
    ws.

% Maintenance statements — probed slice #71. EMPTY, OPTIMIZE,
% VERIFY, REPAIR, UPGRADE all share the same shape:
%
%   <KEYWORD> TABLE <name>
%
% Bare keyword rejected with "Missing TABLE". `KEYWORD <name>` (no
% TABLE) rejected with "Expected TABLE but instead found <name>".
% `KEYWORD TABLE` alone rejected with "Missing table name". So the
% three tokens are all mandatory. EMPTY TABLE accepted operationally
% (truncates); the others returned 0x2c18 in our test env (syntax
% admissible, operational rejection on the temp table).
%
% AST: `maint(Op, Table)` where Op ∈ {empty, optimize, verify, repair, upgrade}.
maint_statement(maint(Op, Table)) -->
    maint_op(Op),
    ws1, keyword(table),
    ws1, identifier(Table),
    ws.

maint_op(empty)    --> keyword(empty).
maint_op(optimize) --> keyword(optimize).
maint_op(verify)   --> keyword(verify).
maint_op(repair)   --> keyword(repair).
maint_op(upgrade)  --> keyword(upgrade).

% RENAME TABLE — probed slice #70. DBISAM accepts the single-pair
% form only: `RENAME TABLE <old> TO <new>`. Multi-pair (`old1 TO
% new1, old2 TO new2`) rejected. Bare `RENAME <name>` (no TABLE)
% rejected — TABLE is the only second-token RENAME accepts. The
% operational reqcode in our test env is 0x2c18 (the table can't be
% renamed in this transaction context), but the syntax parses.
rename_statement(rename_table(Old, New)) -->
    keyword(rename),
    ws1, keyword(table),
    ws1, identifier(Old),
    ws1, keyword(to),
    ws1, identifier(New),
    ws.

% ALTER TABLE — DBISAM has a narrow ALTER syntax. Probed slice #68:
% the only clearly-accepted syntactic shape is `ALTER TABLE <name>
% ADD <col> <type>`. Engine rejects standard forms like RENAME
% COLUMN, ALTER COLUMN, DROP COLUMN, ADD COLUMN (with explicit
% COLUMN keyword), MODIFY/CHANGE.
%
% Other potentially-accepted forms (ADD PRIMARY KEY, etc.) exist but
% the engine errors with reqcode 0x2b05 (operational, not syntax)
% in our test environment — syntax is admissible but our temp
% tables can't accept the operation. Minimal grammar coverage for
% now; extend when a corpus entry demands a richer form.
alter_table_statement(alter_table(Table, add_column(Col, Type))) -->
    keyword(alter),
    ws1, keyword(table),
    ws1, identifier(Table),
    ws1, keyword(add),
    ws1, identifier(Col),
    ws1, cast_type(Type),
    ws.

% Left-associative accumulator: `a UNION b UNION c` -> union(union(a,b),c).
% Try UNION ALL before bare UNION because of the longer keyword prefix.
union_rest(L, S) -->
    ws1, keyword(union),
    ws1, keyword(all),
    ws1, select_statement(R),
    union_rest(union_all(L, R), S).
union_rest(L, S) -->
    ws1, keyword(union),
    ws1, select_statement(R),
    union_rest(union(L, R), S).
union_rest(S, S) --> [].

% SELECT INTO — DBISAM's CTE-substitute pattern. Syntax:
%   SELECT <cols> INTO <table> FROM <rest>
% INTO sits between the column list and FROM. Probed slice (#58):
% creates a real table as a side effect, so the engine fully accepts
% it; the grammar's job is purely shape. AST wraps the regular SELECT
% in `select_into(Table, select_statement(...))` per option (c) of
% the slice plan — minimal migration. Try the INTO form first so the
% INTO keyword gets consumed before the no-INTO rule's mandatory
% `FROM` match attempts.
select_statement(select_into(Table, select_statement(Distinct, Columns, FromList, Mods))) -->
    keyword(select),
    ws1,
    distinct_opt(Distinct),
    column_list(Columns),
    ws1, keyword(into),
    ws1, identifier(Table),
    ws1, keyword(from),
    ws1,
    table_ref_list(FromList),
    modifier_list(Mods),
    ws.
select_statement(select_statement(Distinct, Columns, FromList, Mods)) -->
    keyword(select),
    ws1,
    distinct_opt(Distinct),
    column_list(Columns),
    ws1,
    keyword(from),
    ws1,
    table_ref_list(FromList),
    modifier_list(Mods),
    ws.

% Optional DISTINCT (or ALL) flag immediately after SELECT. DBISAM
% accepts both forms; ALL is the default behaviour, so we keep the
% AST representation simple — `all_rows` for default/ALL, `distinct`
% for DISTINCT.
distinct_opt(distinct) --> keyword(distinct), ws1.
distinct_opt(all_rows) --> keyword(all), ws1.
distinct_opt(all_rows) --> [].

% Comma-separated table references with optional aliases, plus
% explicit JOINs:
%   FROM customer
%   FROM customer c
%   FROM customer AS c
%   FROM orderh, orderi                       -- comma form
%   FROM a JOIN b ON pred                     -- bare JOIN = INNER
%   FROM a INNER JOIN b ON pred
%   FROM a LEFT JOIN b ON pred
%   FROM a LEFT OUTER JOIN b ON pred          -- OUTER optional
%   FROM a RIGHT JOIN b ON pred
%   FROM a LEFT JOIN b ON p1 LEFT JOIN c ON p2  -- chained, left-assoc
% Each list element is either:
%   table(identifier(Name), Alias)
%   joined(Type, Left, Right, OnPred)
% where Type ∈ {inner, left, right} and Left/Right are themselves
% table-or-joined nodes. Chains nest as
%   joined(left, joined(left, A, B, P1), C, P2).
table_ref_list([T|Ts]) -->
    table_or_joined(T),
    table_ref_list_rest(Ts).

table_ref_list_rest([]) --> [].
table_ref_list_rest([T|Ts]) -->
    ws, [','], ws,
    table_or_joined(T),
    table_ref_list_rest(Ts).

% Parse a single comma-list element. Tail-recursive accumulator over
% any chain of JOIN ... ON ... clauses; left-associative.
table_or_joined(T) -->
    table_ref(T0),
    join_rest(T0, T).

join_rest(L, T) -->
    ws1, join_type(Type),
    ws1, table_ref(R),
    ws1, keyword(on),
    ws1, predicate_or(OnPred),
    join_rest(joined(Type, L, R, OnPred), T).
join_rest(T, T) --> [].

% Try the longest prefixes first: LEFT OUTER JOIN before LEFT JOIN.
% DBISAM does not support FULL OUTER / CROSS / USING — rejected at
% parse time by the engine, so the grammar omits them too.
join_type(left)  --> keyword(left),  ws1, keyword(outer), ws1, keyword(join).
join_type(left)  --> keyword(left),  ws1, keyword(join).
join_type(right) --> keyword(right), ws1, keyword(outer), ws1, keyword(join).
join_type(right) --> keyword(right), ws1, keyword(join).
join_type(inner) --> keyword(inner), ws1, keyword(join).
join_type(inner) --> keyword(join).

% Try aliased forms first. Order: explicit `AS` → implicit-with-guard
% → bare. The implicit-alias guard rejects following identifiers that
% are actually next-clause keywords (WHERE, GROUP, TOP, ORDER, HAVING,
% UNION) — without it, `FROM customer WHERE ...` would consume `WHERE`
% as a column alias.
%
% Table names can be catalog-qualified per DBISAM's `<catalog>\<table>`
% notation (e.g. `NISAINT_CS\Analysis`). The catalog_qualified term
% wraps the catalog + table identifiers.
table_ref(table(N, A)) -->
    table_name(N),
    ws1, keyword(as),
    ws1, identifier(A).
table_ref(table(N, identifier(AliasAtom))) -->
    table_name(N),
    ws1, identifier(identifier(AliasAtom)),
    { atom_lower(AliasAtom, Lower),
      \+ is_table_terminator(Lower) }.
table_ref(table(N, no_alias)) -->
    table_name(N).

% Table name with optional catalog prefix.
table_name(catalog_qualified(C, T)) -->
    identifier(C), ['\\'], identifier(T).
table_name(N) --> identifier(N).

% Lowercase keywords that, if seen after a table name in identifier
% form, are NOT aliases. Match is case-insensitive via atom_lower/2.
is_table_terminator(where).
is_table_terminator(group).
is_table_terminator(top).
is_table_terminator(order).
is_table_terminator(having).
is_table_terminator(union).
is_table_terminator(as).
is_table_terminator(inner).
is_table_terminator(left).
is_table_terminator(right).
is_table_terminator(outer).
is_table_terminator(join).
is_table_terminator(on).

atom_lower(A, L) :-
    atom_chars(A, Cs),
    maplist(to_lower, Cs, LCs),
    atom_chars(L, LCs).

% Optional modifier clauses, source-order: ... TOP n (more come later).
% Each modifier is preceded by whitespace; the list is empty for a
% bare SELECT-FROM.
modifier_list([M|Ms]) -->
    ws1,
    modifier(M),
    modifier_list(Ms).
modifier_list([]) --> [].

modifier(where(P)) -->
    keyword(where),
    ws1,
    predicate_or(P).
modifier(group_by(Cs)) -->
    keyword(group),
    ws1, keyword(by),
    ws1,
    group_by_list(Cs).

% GROUP BY items are full value expressions — DBISAM accepts qualified
% refs (`group by orderh.ref`), function calls, etc. Bare identifiers
% are just one shape `value//1` produces, so the existing
% `group_by([identifier(a), ...])` shape stays compatible.
group_by_list([V|Vs]) -->
    value(V),
    group_by_list_rest(Vs).
group_by_list_rest([]) --> [].
group_by_list_rest([V|Vs]) -->
    ws, [','], ws,
    value(V),
    group_by_list_rest(Vs).
modifier(order_by(Os)) -->
    keyword(order),
    ws1, keyword(by),
    ws1,
    order_item_list(Os).
% HAVING — engine-validated to require GROUP BY, and to constrain
