% Left operand: wrap if it's an arith with strictly lower precedence
% than the surrounding op. Same-precedence left operands need no
% parens — left-associative grouping is the parser default.
gen_arith_left(arith(LOp, L1, R1), P, Chars) :-
    arith_prec(LOp, LP),
    LP < P, !,
    gen_value(arith(LOp, L1, R1), Inner),
    append([['('], Inner, [')']], Chars).
gen_arith_left(V, _, Chars) :- gen_value(V, Chars).

% Right operand: wrap if it's an arith with precedence less-than-or-equal
% to the surrounding op. Equal precedence on the right needs parens
% because left-assoc reparse would re-group otherwise.
gen_arith_right(arith(ROp, L1, R1), P, Chars) :-
    arith_prec(ROp, RP),
    RP =< P, !,
    gen_value(arith(ROp, L1, R1), Inner),
    append([['('], Inner, [')']], Chars).
gen_arith_right(V, _, Chars) :- gen_value(V, Chars).

