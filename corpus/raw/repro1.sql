SELECT 
	ORDERH.COMPANY as Customer, 
	'Order' as Type, 
	ORDERH.SEQTYPE as RangeCode, 
	ORDERH.REF as Reference, 
	ORDERH.UF_SHIPMENTREF as "Shipment Ref", 
	ORDERH.INCOTERMS as "Incoterms",
	ORDERH.CONSCODE as "WH Account", 
	ORDERH.UF_WHORDERNO as "WH Order No", 
	ORDERH.UF_FRTPALLQTYN as "Exp Pallets", 
	ORDERH.VOLUME as "Volume", 
	ORDERH.UF_EXPREPRODATE as "Exp Repro Date", 
	ORDERH.UF_SHIPOUT as "Ship Out Date", 
	CAST(ORDERH.UF_WHINSTR2 as VARCHAR(255)) as "Repro Instructions", 
	uf_csa as "CSEA", 
	RIGEOGRAPHIC.RITERRITORYDESC as "RI Territory", 
	RIGEOGRAPHIC.COUNTRYNAME as "Country", 
	RIGEOGRAPHIC.REGIONDESC as "Region", 
	ORDERH.UF_FINALADD as "Deliver Address",
	ORDERH.UF_FREIGHT1 as "Forwarder"
FROM 
	ORDERH 
	left join RIGEOGRAPHIC on ORDERH.TERR = RIGEOGRAPHIC.REGIONCODE
WHERE 
	ORDERH.OHINVREF IS NULL
	AND ORDERH.INSTATUS=1
	AND ORDERH.UF_EXPREPRODATE IS NOT NULL

UNION ALL

SELECT 
	QUOTEH.COMPANY as Customer, 
	'QUOTE' as Type, 
	QUOTEH.SEQTYPE as RangeCode, 
	QUOTEH.REF as Reference, 
	QUOTEH.UF_SHIPMENTREF as "Shipment Ref", 
	QUOTEH.INCOTERMS as "Incoterms",
	QUOTEH.CONSCODE as "WH Account", 
	QUOTEH.UF_WHORDERNO as "WH QUOTE No", 
	QUOTEH.UF_FRTPALLQTYN as "Exp Pallets", 
	QUOTEH.VOLUME as "Volume", 
	QUOTEH.UF_EXPREPRODATE as "Exp Repro Date", 
	QUOTEH.UF_SHIPOUT as "Ship Out Date", 
	CAST(QUOTEH.UF_WHINSTR2 as VARCHAR(255)) as "Repro Instructions", 
	uf_csa as "CSEA", 
	RIGEOGRAPHIC.RITERRITORYDESC as "RI Territory", 
	RIGEOGRAPHIC.COUNTRYNAME as "Country", 
	RIGEOGRAPHIC.REGIONDESC as "Region", 
	QUOTEH.UF_FINALADD as "Deliver Address",
	QUOTEH.UF_FREIGHT1 as "Forwarder"
FROM 
	QUOTEH 
	left join RIGEOGRAPHIC on QUOTEH.TERR = RIGEOGRAPHIC.REGIONCODE
WHERE 
	QUOTEH.QTRESULTORDERREF IS NULL
	AND QUOTEH.INSTATUS=1
	AND QUOTEH.UF_EXPREPRODATE IS NOT NULL

